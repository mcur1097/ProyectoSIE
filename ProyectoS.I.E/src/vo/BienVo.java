package vo;

public class BienVo {
	
	private String placa;
	private String serialb;
	private String nombreBien;
	private String descripcion;
	private String modelo;
	private String marca;
	private String fechaIngreso;
	private float valor;
	private int areaAm;
	private int cedula;
	
	public String getPlaca() {
		return placa;
	}
	public void setPlaca(String placa) {
		this.placa = placa;
	}
	public String getSerialb() {
		return serialb;
	}
	public void setSerialb(String serialb) {
		this.serialb = serialb;
	}
	public String getNombreBien() {
		return nombreBien;
	}
	public void setNombreBien(String nombreBien) {
		this.nombreBien = nombreBien;
	}
	public String getDescripcion() {
		return descripcion;
	}
	public void setDescripcion(String descripcion) {
		this.descripcion = descripcion;
	}
	public String getModelo() {
		return modelo;
	}
	public void setModelo(String modelo) {
		this.modelo = modelo;
	}
	public String getMarca() {
		return marca;
	}
	public void setMarca(String marca) {
		this.marca = marca;
	}
	public String getFechaIngreso() {
		return fechaIngreso;
	}
	public void setFechaIngreso(String fechaIngreso) {
		this.fechaIngreso = fechaIngreso;
	}
	public float getValor() {
		return valor;
	}
	public void setValor(float valor) {
		this.valor = valor;
	}
	public int getAreaAm() {
		return areaAm;
	}
	public void setAreaAm(int areaAm) {
		this.areaAm = areaAm;
	}
	public int getCedula() {
		return cedula;
	}
	public void setCedula(int cedula) {
		this.cedula = cedula;
	}

}
